    private void btnNuevoPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoPacienteActionPerformed
        JFileChooser selectorArchivos = new JFileChooser();
        selectorArchivos.showOpenDialog(this);
        
        String mensajeNuevoPasiente = ""; 
        String selectArchivoZIP = selectorArchivos.getSelectedFile().getAbsolutePath();
        
        try {			
			ZipFile zipFile = new ZipFile(selectArchivoZIP);
		 
//			if (zipFile.isEncrypted()) {		
//				zipFile.setPassword("test123!");
//			}
			
			List fileHeaderList = zipFile.getFileHeaders();			
		
			for (int i = 0; i < fileHeaderList.size(); i++) {
				FileHeader fileHeader = (FileHeader)fileHeaderList.get(i);
				zipFile.extractFile(fileHeader, "Paciente");
			}
			mensajeNuevoPasiente = "Nueva pasiente creada ";
		} catch (ZipException e) {
			mensajeNuevoPasiente = e.toString();
		}
        jtDirecorioDICOM.setModel(new FileSystemModel(new File("Paciente")));
        JOptionPane.showMessageDialog(null, mensajeNuevoPasiente,"Pacientes", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_btnNuevoPacienteActionPerformed

