    public static boolean deleteDir(File dir) {
      if (dir.isDirectory()) {
         String[] children = dir.list();
         for (int i = 0; i < children.length; i++) {
            boolean success = deleteDir (new File(dir, children[i]));
            
            if (!success) {
               return false;
            }
         }
      }
      return dir.delete();
   }
    
    private void cargarMostrar(File seleccionDICOM){
        try {
            SourceImage sImgDICOM = new SourceImage(seleccionDICOM.getAbsolutePath());
            Image ImgDICOM = sImgDICOM.getBufferedImage().getScaledInstance(lblIconDICOM.getWidth(), lblIconDICOM.getHeight(), Image.SCALE_SMOOTH);
            ImageIcon iconDICOM = new ImageIcon(ImgDICOM);
            lblIconDICOM.setIcon(iconDICOM);       
            
        } catch (IOException | DicomException ex) {
            Logger.getLogger(frmPredictionBreastCancer.class.getName()).log(Level.SEVERE, null, ex);
        }          
    }    

